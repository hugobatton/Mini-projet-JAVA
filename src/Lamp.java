/**
 * this class define a Lamp by 2 details and one boolean which inform on its state.
 *
 */



public class Lamp {
	
	private int x;
	private int y;
	private boolean ESTALLUME;
	
	public Lamp (int x , int y, boolean ESTALLUME )
	
	{
		
		this.x=x;
		this.y=y;
		this.ESTALLUME=ESTALLUME;
		
	}
	
	

}
